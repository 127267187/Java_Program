package Practice;

public class PositiveAndNegative_NumCheck {
	
	public static void main(String[] args) {
		
		double num = 0.00;
		
		if(num>0) 
		{
			System.out.println(num + "No is Positive");
		}
		else if(num<0) 
		{
			System.out.println(num + "No is Negative");
		}
		else 
		{
			System.out.println(num + "No is Zero");
		}
	}

}
